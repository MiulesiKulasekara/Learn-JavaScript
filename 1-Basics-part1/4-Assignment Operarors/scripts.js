
// -----Assignment Operators-----

// Equal
var myName = "Miulesi";
console.log(myName);

// Addition with equal
var age = 22;
age += 10; // age = age + 10 = 32
console.log(age);//32

// Substraction with equal
age -= 10; // age = age - 10 = 32 - 10 = 22
console.log(age);//22

// Multiplication with equal
age *= 10; // age = age * 10 = 22 * 10 = 220
console.log(age);//220

// Division with equal
age /= 4; // age = age / 4 = 220 / 4 = 55
console.log(age);//55

// Modulus with equal
age %= 8; // age = age % 8 = 55 / 6 = 7
console.log(age);//7

// Exponentiation with equal
age **= 2;//age = age ^^ 2 = 7 ^^ 2 = 49
console.log(age);//49